package Basics;

public class eighteenth{
    public static void main(String[] args) {
        //Final keyword

        double pi = 3.14159;

        pi = 4;

        System.out.println(pi);

        //when you use the final, you can not change the value anymore
        final double pii = 3890.8;
        //pii = 21;
        System.out.println(pii);
    }
}